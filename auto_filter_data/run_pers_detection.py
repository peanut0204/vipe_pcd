def parse_args():
    parser = argparse.ArgumentParser()
    parser.add_argument(
        "--data_root",
        type=str,
        default="data/360-videos-new",
        help="path to video data_root to process",
    )
    parser.add_argument(
        "--output_root", type=str, default="./filter_results/pers_detection"
    )
    parser.add_argument(
        "--video_list",
        type=str,
        # default="metadata/video_list_benchmark_500.csv",
        default="video_list.csv",
    )
    parser.add_argument("--start", type=int, default=0)
    parser.add_argument("--end", type=int, default=-1)
    args = parser.parse_args()
    return args


@torch.no_grad()
def pers_detection(video_path, device="cpu", num_frames=4):
    info = utils.get_video_info(video_path)
    total_frames = info["total_frames"]

    indices = np.linspace(0, total_frames - 1, num=num_frames, dtype=int)
    # (num_frames, H, W, 3)
    frames_rgb = utils.read_video(video_path, indices, return_float=False)

    num_frames, height, width = frames_rgb.shape[:3]

    # RGB to Gray
    # (num_frames, H, W)
    frames_gray = np.zeros_like(frames_rgb[..., 0])
    for i in range(num_frames):
        frames_gray[i] = cv2.cvtColor(frames_rgb[i], cv2.COLOR_RGB2GRAY)

    # (num_frames, 1, H, W)
    frames = torch.from_numpy(frames_gray / 255.0).unsqueeze(1)
    # Roll the frame 180 degrees (on the width dimension) to make the middle line to the center
    frames = torch.roll(frames, width // 2, dims=-1)
    # keep the middle line
    frames = frames[
        :, :, :, width // 2 - 3 : width // 2 + 3
    ]  # (frame_count, 1, H, 6)

    # Sobel filter: (3, 3)
    sobel = torch.tensor([[1, 0, -1], [2, 0, -2], [1, 0, -1]])
    # (frame_count, H, 4)
    sobel_mag = (
        torch.nn.functional.conv2d(
            frames.float().to(device),
            sobel.unsqueeze(0).unsqueeze(0).float().to(device),
        )
        .abs()
        .squeeze(1)
    )
    return sobel_mag.mean().cpu().numpy()


def main():
    args = parse_args()
    filelist = utils.read_video_list(args.video_list, args.start, args.end)
    print(f"Found {len(filelist)} total videos to process.")

    undo_list = []
    for file in filelist:
        category, video_id, clip_id = (
            file.split(",")[0],
            file.split(",")[1],
            file.split(",")[2].split(".")[0],
        )
        file_path = Path(
            args.data_root, category, video_id, clip_id
        ).with_suffix(".mp4")

        save_path = Path(args.output_root) / file_path.relative_to(
            args.data_root
        )
        save_path = save_path.with_suffix(".txt")
        if not save_path.exists():
            undo_list.append(file)

    print(f"Found {len(undo_list)} undo videos to process.")
    filelist = undo_list

    for file in tqdm(filelist):
        category, video_id, clip_id = (
            file.split(",")[0],
            file.split(",")[1],
            file.split(",")[2].split(".")[0],
        )

        file_path = Path(
            args.data_root, category, video_id, clip_id
        ).with_suffix(".mp4")

        if not file_path.exists():
            print(f"Video {file_path} does not exist.")
            continue

        save_path = Path(args.output_root) / file_path.relative_to(
            args.data_root
        )
        save_path = save_path.with_suffix(".txt")
        if save_path.exists():
            print(f"Skip {save_path} since it already exists")
            continue

        save_path.parent.mkdir(parents=True, exist_ok=True)
        try:
            result = pers_detection(file_path, num_frames=4)
        except Exception as e:
            print(f"Error processing {file_path}: {e}")
        else:
            with open(save_path, "w") as f:
                f.write(f"{result:.6f}")


if __name__ == "__main__":
    main()


